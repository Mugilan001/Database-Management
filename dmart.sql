create database Dmart;
use Dmart;
CREATE TABLE Customers (
    CustomerID INT AUTO_INCREMENT PRIMARY KEY,
    Name VARCHAR(100) NOT NULL,
    Email VARCHAR(100) UNIQUE,
    PhoneNumber VARCHAR(15),
    Address TEXT,
    RegistrationDate TIMESTAMP DEFAULT CURRENT_TIMESTAMP 
);

    CREATE TABLE Products (
    ProductID INT AUTO_INCREMENT PRIMARY KEY,
    ProductName VARCHAR(100) NOT NULL,
    Category VARCHAR(50),
    Price DECIMAL(10, 2) NOT NULL,
    StockQuantity INT NOT NULL,
    ReorderLevel INT DEFAULT 10
);
CREATE TABLE Sales (
    SaleID INT AUTO_INCREMENT PRIMARY KEY,
    CustomerID INT,
    ProductID INT,
    SaleDate DATETIME DEFAULT CURRENT_TIMESTAMP,
    Quantity INT NOT NULL,
    TotalAmount DECIMAL(10, 2),
    FOREIGN KEY (CustomerID) REFERENCES Customers(CustomerID),
    FOREIGN KEY (ProductID) REFERENCES Products(ProductID)
);


DELIMITER $$
CREATE TRIGGER CalculateTotalAmount
BEFORE INSERT ON Sales
FOR EACH ROW
BEGIN
    DECLARE product_price DECIMAL(10, 2);
    SELECT Price INTO product_price FROM Products WHERE ProductID = NEW.ProductID;
    SET NEW.TotalAmount = NEW.Quantity * product_price;
END$$
DELIMITER ;
CREATE TABLE Inventory (
    InventoryID INT AUTO_INCREMENT PRIMARY KEY,
    ProductID INT,
    RestockDate DATETIME DEFAULT CURRENT_TIMESTAMP,
    QuantityAdded INT NOT NULL,
    FOREIGN KEY (ProductID) REFERENCES Products(ProductID)
);
CREATE TABLE Revenue (
    RevenueID INT AUTO_INCREMENT PRIMARY KEY,
    RevenueDate  DATETIME DEFAULT CURRENT_TIMESTAMP,
    TotalRevenue DECIMAL(15, 2),
    FOREIGN KEY (RevenueDate) REFERENCES Sales(SaleDate)
);
ALTER TABLE Sales ADD INDEX (SaleDate);
